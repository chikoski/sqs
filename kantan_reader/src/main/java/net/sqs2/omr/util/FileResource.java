package net.sqs2.omr.util;

public class FileResource{
		byte[] bytes;
		long lastModified;
		
		public FileResource(byte[] bytes, long lastModified){
			this.bytes = bytes;
			this.lastModified = lastModified;
		}
		
		public byte[] getBytes(){
			return this.bytes;
		}

		public long getLastModified(){
			return this.lastModified;
		}
}