package net.sqs2.omr.result.contents.chart;

public class LegendHandler{
	Legend[] legendArray;
	String axis = null;
	public LegendHandler(Legend[] legendArray, String axis){
		this.legendArray = legendArray;
		this.axis = axis;
	}
	
	public Legend[] getLegendArray() {
		return legendArray;
	}

	public String getAxis() {
		return axis;
	}
	
}