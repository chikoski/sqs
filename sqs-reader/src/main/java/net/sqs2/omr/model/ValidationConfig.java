package net.sqs2.omr.model;

import java.io.Serializable;

public class ValidationConfig implements Serializable {
	private static final long serialVersionUID = 0L;
	boolean checkUpsideDown;
	boolean checkEvenOdd;
	boolean checkDistortion;
	double horizontalDistortion;
	double verticalDistortion;

	public ValidationConfig() {
	}

	public boolean isCheckEvenOdd() {
		return this.checkEvenOdd;
	}

	public void setCheckEvenOdd(boolean checkEvenOdd) {
		this.checkEvenOdd = checkEvenOdd;
	}

	public boolean isCheckUpsideDown() {
		return this.checkUpsideDown;
	}

	public void setCheckUpsideDown(boolean checkUpsideDown) {
		this.checkUpsideDown = checkUpsideDown;
	}

	public boolean isCheckDistortion() {
		return this.checkDistortion;
	}

	public void setCheckDistortion(boolean checkDistiortion) {
		this.checkDistortion = checkDistiortion;
	}

	public double getHorizontalDistortion() {
		return this.horizontalDistortion;
	}

	public void setHorizontalDistortion(double horizontalDistortion) {
		this.horizontalDistortion = horizontalDistortion;
	}

	public double getVerticalDistortion() {
		return this.verticalDistortion;
	}

	public void setVerticalDistortion(double verticalDistortion) {
		this.verticalDistortion = verticalDistortion;
	}
}
