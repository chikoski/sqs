package net.sqs2.swing;

import java.io.File;

public class ImageThumbnailTableCell {
	File root;
	String path;
	int index;
	
	public ImageThumbnailTableCell(File root, String path, int index) {
		super();
		this.root = root;
		this.path = path;
		this.index = index;
	}

	public File getRoot() {
		return this.root;
	}

	public String getPath() {
		return this.path;
	}
	
	public int getIndex(){
		return this.index;
	}

}
